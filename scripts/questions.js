// ----- Variables to Track Quiz Progress -----
let currentQuestionIndex = 0;
const userAnswers = [];

// ----- Render the Current Question -----
function renderQuestion() {
  const questionContainer = document.getElementById("question-container");
  const currentQuestion = questions[currentQuestionIndex];

  // Clear any previous content
  questionContainer.innerHTML = "";

  // Set the background image for the page
  document.body.style.background = "url('/assets/bg1.webp') center center / cover no-repeat";

  // Display the header image
  const headerImage = document.createElement("img");
  headerImage.src = `/assets/headerq${currentQuestionIndex + 1}.webp`;
  headerImage.src = `/assets/Headerq${currentQuestionIndex + 1}.webp`;
  headerImage.alt = `Header for Question ${currentQuestionIndex + 1}`;
  headerImage.className = "header-image";
  questionContainer.appendChild(headerImage);

  // Create a container (rectangle) for the question content
  const rectangle = document.createElement("div");
  rectangle.className = "rectangle";

  // Dynamically load the first textq image
  const textImage1 = document.createElement("img");
  textImage1.src = `/assets/textq${currentQuestionIndex + 1}.webp`;
  textImage1.src = `/assets/Textq${currentQuestionIndex + 1}.webp`;
  textImage1.alt = `First Text for Question ${currentQuestionIndex + 1}`;
  textImage1.className = "text-image";
  rectangle.appendChild(textImage1);

  // Conditionally add the question text image (if available)
  const questionTextImage = document.createElement("img");
  questionTextImage.src = `/assets/textquestion${currentQuestionIndex + 1}.webp`;
  questionTextImage.src = `/assets/Textquestion${currentQuestionIndex + 1}.webp`;
  questionTextImage.alt = `Question Text Image ${currentQuestionIndex + 1}`;
  questionTextImage.className = "question-image";
  questionTextImage.onerror = () => {
    // Hide the image if it doesn't exist
    questionTextImage.style.display = "none";
  };
  rectangle.appendChild(questionTextImage);

  // Add a divider image
  const dividerImage = document.createElement("img");
  dividerImage.src = `/assets/dividershortened.webp`;
  dividerImage.alt = `Divider for Question ${currentQuestionIndex + 1}`;
  dividerImage.className = "divider-image";
  rectangle.appendChild(dividerImage);

  // Add the options for this question
  const optionsContainer = document.createElement("div");
  optionsContainer.className = "options-container";
  currentQuestion.options.forEach((option) => {
    const label = document.createElement("label");
    const input = document.createElement("input");
    input.type = "radio";
    input.name = `q${currentQuestionIndex}`;
    input.value = option;

    // Restore a previously selected answer if it exists
    if (userAnswers[currentQuestionIndex] === option) {
      input.checked = true;
      // Make sure the Next button is visible if an answer is already chosen
      document.getElementById("nextButton").style.display = "inline-block";
    }

    // Show the Next button when the user selects an option
    input.addEventListener("change", () => {
      document.getElementById("nextButton").style.display = "inline-block";
    });

    label.appendChild(input);
    label.appendChild(document.createTextNode(option));
    optionsContainer.appendChild(label);
  });
  rectangle.appendChild(optionsContainer);
  questionContainer.appendChild(rectangle);

  // ----- Configure the Back Button -----
  const backButton = document.getElementById("backButton");
  backButton.style.display = "inline-block";
  if (currentQuestionIndex === 0) {
    backButton.textContent = "Exit to Mobile"; // At start of test
    backButton.onclick = () => {
      window.location.href = "index.html";
    };
  } else {
    backButton.textContent = "Back"; // Mid-test navigation
    backButton.onclick = () => {
      currentQuestionIndex--;
      renderQuestion();
    };
  }

  // ----- Configure the Next/Exit Button -----
  const nextButton = document.getElementById("nextButton");
  nextButton.style.display = "none"; // Hide by default until an option is selected
  if (currentQuestionIndex < questions.length - 1) {
    nextButton.src = "/assets/next.webp";
    nextButton.onclick = () => {
      saveAnswer();
      currentQuestionIndex++;
      renderQuestion();
    };
  } else {
    // Last question: set up the Exit button
    nextButton.src = "/assets/exit.webp";
    nextButton.onclick = () => {
      saveAnswer(); // Save the final answer

      // Pass only the userAnswers through query parameters (since the questions are static on scorepage.js)
      const queryParams = new URLSearchParams({
        userAnswers: JSON.stringify(userAnswers)
      });
      console.log(`Redirecting to: scorepage.html?${queryParams.toString()}`);
      window.location.href = `scorepage.html?${queryParams.toString()}`;
    };
  }
}

// ----- Save the Selected Answer -----
function saveAnswer() {
  const selectedOption = document.querySelector(`input[name="q${currentQuestionIndex}"]:checked`);
  userAnswers[currentQuestionIndex] = selectedOption ? selectedOption.value : null;
}

// ----- Initialize the Quiz on Window Load -----
window.onload = renderQuestion;